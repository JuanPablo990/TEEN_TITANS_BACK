package eci.edu.dosw.parcial.TEEN_TITANS_BACK.model;

public class studyPlan {
    private int id;
    private String name;
    private int totalCredits;
    private int totalCourses;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getTotalCredits() {
        return totalCredits;
    }

    public int getTotalCourses() {
        return totalCourses;
    }
}